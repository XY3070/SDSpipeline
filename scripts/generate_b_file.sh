#!/bin/bash
set -euo pipefail
CHR="$1"
OUTDIR="$2"
PREFIX="${3:-chr${CHR}}"

mkdir -p "$OUTDIR"

BFILE="$OUTDIR/${PREFIX}_b_file.txt"

# GRCh38 着丝粒坐标 (UCSC)
declare -A CENTROMERE_COORDS=(
    ["1"]="122026459 124932724" ["2"]="92188145 94090557" ["3"]="90772458 93655574"
    ["4"]="49712061 51743951" ["5"]="46485900 50059807" ["6"]="58553888 59829934"
    ["7"]="58169653 61528020" ["8"]="44033744 45877265" ["9"]="43389635 45518558"
    ["10"]="39686682 41593521" ["11"]="51078348 54425074" ["12"]="34769407 37185252"
    ["13"]="16000000 18051248" ["14"]="16000000 18173523" ["15"]="17083673 19725254"
    ["16"]="36311158 38265669" ["17"]="22813679 26616164" ["18"]="15460899 20861206"
    ["19"]="24498980 27190874" ["20"]="26436232 30038348" ["21"]="10864560 12915808"
    ["22"]="12954788 15054318"
    ["X"]="58100000 63800000"
)
declare -A CHR_LENGTHS=(
    ["1"]=248956422 ["2"]=242193529 ["3"]=198295559 ["4"]=190214555
    ["5"]=181538259 ["6"]=170805979 ["7"]=159345973 ["8"]=145138636
    ["9"]=138394717 ["10"]=133797422 ["11"]=135086622 ["12"]=133275309
    ["13"]=114364328 ["14"]=107043718 ["15"]=101991189 ["16"]=90338345
    ["17"]=83257441 ["18"]=80373285 ["19"]=58617616 ["20"]=64444167
    ["21"]=46709983 ["22"]=50818468
    ["X"]=156040895
)

read C_START C_END <<< "${CENTROMERE_COORDS[$CHR]}"
MAX_LEN=${CHR_LENGTHS[$CHR]}

if [[ -z "${C_START:-}" || -z "${C_END:-}" || -z "${MAX_LEN:-}" ]]; then
    echo "[Error] Unsupported chromosome: $CHR" >&2
    exit 1
fi

# 输出两行边界，跳过着丝粒区间本身
echo "1	$((C_START - 1))" > "$BFILE"
echo "$((C_END + 1))	$MAX_LEN" >> "$BFILE"
